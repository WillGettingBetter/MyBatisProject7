package com.it.entity;

/**
 * 模糊查询需要用到的name
 * 
 * @author SJF18
 * 
 */
public class NameInfo {

	private String name;

	public NameInfo() {
		super();
	}

	public NameInfo(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
